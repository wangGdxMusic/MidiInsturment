package com.kw.gdx.utils.ads.bean;

public class PhoneInfo {
    private int ch;
    private int w;
    private int h;
    private int cc;

    public void setCh(int ch) {
        this.ch = ch;
    }

    public int getCh() {
        return ch;
    }

    public int getW() {
        return w;
    }

    public void setW(int w) {
        this.w = w;
    }

    public int getH() {
        return h;
    }

    public void setH(int h) {
        this.h = h;
    }

    public int getCc() {
        return cc;
    }

    public void setCc(int cc) {
        this.cc = cc;
    }
}
